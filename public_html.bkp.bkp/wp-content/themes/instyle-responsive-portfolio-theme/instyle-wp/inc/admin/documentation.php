          <div id="installation" class="block">
          	<h3>Installation & Setup</h3>
            <p>You need a working verion of Wordpress 3.0 and above.</p>
            <p>I'll be making lots of references to the Worpdress admin panel in this documentation. If you're totally new to Wordpress, take a few minutes to familiarize yourself with how the admin panel is laid out so   you won't be lost when I instruct you to open up &quot;Appearance &gt;   Widgets&quot; or something like that.</p>
            <p>Also, its higly recommended that you familiarize with <a href="http://codex.wordpress.org/Custom_Post_Types">Wordpress 3.0 Custom Post Types</a> and <a href="http://codex.wordpress.org/Post_Formats">Post Formats</a>. This is the key to using the blog and the portfolio sections.</p>
            <p><strong>Installing the Theme </strong></p>
            <p>You need to upload the theme files to your Wordpress installation's <strong>wp-content/themes/</strong> folder. Then go to your Wordpress administration panel and activate the theme from the Appearance &gt; Themes pane.</p>
            <p><strong>DO NOT install it like wp-conten/themes/themename/themename</strong>/...</p>
            <p><strong>DO install it like wp-content/themes/themename</strong></p>
            <p>If you want to install the theme by uploading the zip file, please make sure that you do not upload the zip file downloaded directly from Themeforest. The zip file you just downloaded contains extra files such as documentation and licensing. You should upload the zip file containing the Wordpress files only.</p>
            <p>Once the theme is uploaded, you need to activate it. Go to Appearance > Themes and activate your chosen theme.</p>
