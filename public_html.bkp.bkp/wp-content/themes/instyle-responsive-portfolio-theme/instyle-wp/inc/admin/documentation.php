          <div id="settingupportfolio" class="block">
            <h3>Setting up Portfolio</h3>
            <p>To set up the portfolio section, you must create a new page, you can do so by navigating to <strong>Pages > Add New</strong>. You can give this page a title of "portfolio" yet you do not have to include any content. Select either of the "Portfolio" templates from the Page Attributes section and click "Publish". The main portfolio section can now be viewed by visiting the page you just created.</p>
            <p>You can elect to have the portfolio as your homepage by navigating to Settings > Reading and selecting the portfolio page from the “Front page displays” options. If you do, you must create an additional page called “blog” and choose this page as the posts page homepage from the same reading settings.
            <h3>Adding Portfolio Items</h3>
            <p>To add a new portfolio item, navigate to <strong>Portfolio > Add New</strong>. Here you can add a brief description of the project, assign various categories and upload images. When you upload images, make sure you select them as featured images. More information can be found on "Featured Images" section of the documentation.</p>
			<h3>Adding Video or Audio Items</h3>
            <p>When you are adding a portfolio item, you will notice a "Portfolio Settings" metabox below the textarea. You can select the portfolio type from the dropdown:</p>
            <p><ul>
            	<li><strong>Slideshow:</strong> This portfolio type will display your uploaded images in a slideshow format just like the gallery post format.</li>
                <li><strong>Video: </strong>This will display your video on the portfolio item page. You should still upload a featured image to display on the portfolio gallery page</li>
                <li><strong>Audio:</strong> This will show an audio player on the portfolio item page. You should still upload a featured image to display on the portfolio gallery page</li>
             	</ul>
            </p>
          </div>
          <div id="custommenus" class="block">
          <h3>Custom Menus</h3>
            <p>You can setup custom menus to configure your site's navigation. The theme comes with a single custom menu location called "Navigation Bar Menu".</p>
            <p>To setup your custom menus, navigate to <strong>Appearance > Menus</strong>. Give your menu a name and build it up using the available widgets. You can add a variety of items including pages, categories, custom links. To extend the available widgets, click the screen options tab at the very top of the screen and configure your options.</p>
            
            <p>Once you have built and saved your menu, simply select the theme location using the widget on the left. Under "Navigation Bar Menu", select your custom menu from the dropdown list to use it as the site's main navigation.</p> 

          </div>
          <div id="themesidebars" class="block">
          <h3>Sidebars</h3>
          <p>The theme comes with 2 pre-defined sidebars, "Blog" &amp; "Pages".</p>
		  <p>All you need to do is to find the sidebars in the <strong>Appearance > Widgets</strong> menu and then add widgets to those sidebars in Appearance > Widgets.</p>
          <p>You can also create your own sidebars. Go to <strong>Appearance > Sidebars</strong> menu and add sidebars. You can later, go to a page and select which sidebar to use for that page.</p>
