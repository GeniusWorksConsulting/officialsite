          <h3>Post Formats</h3>
          <p>This theme comes bundled with the following post formats:</p>
          <p>
			<ul>
            	<li><strong>Image</strong>: Upload a single image and set it as featured image. </li>
                <li><strong>Gallery</strong>: Upload multiple images </li>
                <li><strong>Video</strong>: Upload your own videos or enter embed codes. </li>
                <li><strong>Audio</strong>: Upload your own mp3 or ogg files here</li>
                <li><strong>Link</strong>: Enter link url and title in the metabox</li>
                <li><strong>Quote</strong>: Enter your favorite quote here</li>
                <li<strong>>Standard</strong>: A standard WordPress post. Upload a single image and set it as featured image.</li>
            </ul>
          </p>
          <p>Some of these post formats will have their own custom options. These custom options will only appear once the format is selected.</p>
